function mp(p) {
    const name = p.n.split(" ")[0];
    const char = npc[name];
    if (!char) {
        t.innerText = `${p.n}: ...`;
        return;
    }
    t.innerText = `${name}: ${char.s}`;
    const img = document.createElement('img');
    img.src = p.face || '';
    img.alt = `${p.n}의 얼굴`;
    img.style.width = '100px';

    const face = document.getElementById('face');
    if (face) {
        face.innerHTML = '';
        face.appendChild(img);
    }

    if (char.a && char.a.length > 0) {
        sc(char.a.map(opt => ({
            t: opt.t,
            f: () => showNext(name, opt.n)
        })));
    } else {
        sc([{ t: "...", f: mm }]);
    }
}

function showNext(name, step) {
    const char = npc[name];
    const next = char[step];
    const face = document.getElementById('face');
    if (step === "end") {
        if (face) face.innerHTML = '';
        mm();
        return;
    }
    if (!next) {
        if (face) face.innerHTML = '';
        return;
    }

    t.innerText = `${name}: ${next.s}`;
    if (next.a && next.a.length > 0) {
        sc(next.a.map(opt => ({
            t: opt.t,
            f: () => showNext(name, opt.n)
        })));
    } else {
        if (face) face.innerHTML = '';
        mm();
    }
}




